    private void btnHapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnHapusActionPerformed
        // TODO add your handling code here:
        try {
        // Mendapatkan indeks baris yang dipilih di tabel
        int selectedRow = table_data_karyawan.getSelectedRow();

        if (selectedRow == -1) {
            JOptionPane.showMessageDialog(this, "Pilih baris yang akan dihapus.", "Peringatan", JOptionPane.WARNING_MESSAGE);
            return; // Keluar dari metode jika tidak ada baris yang dipilih
        }

        // Mendapatkan nilai ID Karyawan dan bulan dari baris yang dipilih
        String idKaryawan = table_data_karyawan.getValueAt(selectedRow, 1).toString();
        String bulan = table_data_karyawan.getValueAt(selectedRow, 0).toString();

        // Menghapus data dari database berdasarkan ID Karyawan dan bulan
        String query = "DELETE FROM gaji_karyawan WHERE id_karyawan = ? AND bulan = ?";
        PreparedStatement pstmt = conn.prepareStatement(query);
        pstmt.setString(1, idKaryawan);
        pstmt.setString(2, bulan);
        pstmt.executeUpdate();

        // Menghapus baris dari tabel
        DefaultTableModel dataModel = (DefaultTableModel) table_data_karyawan.getModel();
        dataModel.removeRow(selectedRow);

        JOptionPane.showMessageDialog(this, "Data berhasil dihapus");

        clear(); // Membersihkan inputan
    } catch (SQLException ex) {
        Logger.getLogger(GUI_Restaurant.class.getName()).log(Level.SEVERE, null, ex);
        JOptionPane.showMessageDialog(this, "Error: " + ex.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
    }
    }//GEN-LAST:event_btnHapusActionPerformed

