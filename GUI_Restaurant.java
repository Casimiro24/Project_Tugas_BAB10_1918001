    private void btnSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimpanActionPerformed
        try {
        koneksi(); // Membuat koneksi ke database
        String bulan = cmb_bulan.getSelectedItem().toString();
        String idKaryawan = txt_id.getText();

        // Periksa apakah data dengan ID karyawan yang sama pada bulan yang sama sudah ada
        String cekQuery = "SELECT * FROM gaji_karyawan WHERE id_karyawan = ? AND bulan = ?";
        PreparedStatement cekPstmt = conn.prepareStatement(cekQuery);
        cekPstmt.setString(1, idKaryawan);
        cekPstmt.setString(2, bulan);
        ResultSet resultSet = cekPstmt.executeQuery();

        if (resultSet.next()) {
            JOptionPane.showMessageDialog(this, "Data dengan ID karyawan yang sama pada bulan yang sama sudah ada.", "Peringatan", JOptionPane.WARNING_MESSAGE);
            return; // Keluar dari metode jika data sudah ada
        }

        // Lanjut menyimpan data jika tidak ada data dengan ID karyawan dan bulan yang sama
        String nama = txt_nama.getText();
        String jenisKelamin = cmb_kelamin.getSelectedItem().toString();
        String jenisKaryawan = cmb_jeniskaryawan.getSelectedItem().toString();
        double gajiPokok = 0.0;

        // Menghitung gaji pokok sesuai jenis karyawan
        if (jenisKaryawan.equals("Karyawan Tetap")) {
            gajiPokok = 4500000.0;
        } else if (jenisKaryawan.equals("Karyawan Magang")) {
            gajiPokok = 2500000.0;
        }

        double potonganPersen = Double.parseDouble(txt_potongangaji.getText());
        double potonganGaji = (potonganPersen / 100) * gajiPokok;
        double gajiSetelahPotongan = gajiPokok - potonganGaji;

        // Format nilai gaji dan potongan ke dalam format rupiah
        String gajiRupiah = String.format("Rp%,.2f", gajiSetelahPotongan);
        String potonganRupiah = String.format("Rp%,.2f", potonganGaji);

        // Menyimpan data ke database
        String query = "INSERT INTO gaji_karyawan (id_karyawan, nama, jenis_kelamin, bulan, jenis_karyawan, gaji) VALUES (?, ?, ?, ?, ?, ?)";
        PreparedStatement pstmt = conn.prepareStatement(query);
        pstmt.setString(1, idKaryawan);
        pstmt.setString(2, nama);
        pstmt.setString(3, jenisKelamin);
        pstmt.setString(4, bulan);
        pstmt.setString(5, jenisKaryawan);
        pstmt.setDouble(6, gajiSetelahPotongan);
        pstmt.executeUpdate();

        // Menampilkan data di tabel
        DefaultTableModel dataModel = (DefaultTableModel) table_data_karyawan.getModel();
        Object[] rowData = {bulan, idKaryawan, nama, jenisKelamin, jenisKaryawan, gajiRupiah, potonganRupiah};
        dataModel.addRow(rowData);

        JOptionPane.showMessageDialog(this, "Data berhasil disimpan");

        clear(); // Membersihkan inputan
    } catch (SQLException ex) {
        Logger.getLogger(GUI_Restaurant.class.getName()).log(Level.SEVERE, null, ex);
        JOptionPane.showMessageDialog(this, "Error: " + ex.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
    }
    }//GEN-LAST:event_btnSimpanActionPerformed

