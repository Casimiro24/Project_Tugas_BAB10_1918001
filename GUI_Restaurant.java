    private void btnSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchActionPerformed
        // TODO add your handling code here:
        try {
        koneksi(); // Membuat koneksi ke database
        String searchText = txtsearch.getText();

        // Query untuk mencari data berdasarkan nama atau user ID
        String query = "SELECT * FROM gaji_karyawan WHERE nama LIKE ? OR id_karyawan LIKE ?";
        PreparedStatement pstmt = conn.prepareStatement(query);
        pstmt.setString(1, "%" + searchText + "%");
        pstmt.setString(2, "%" + searchText + "%");
        ResultSet resultSet = pstmt.executeQuery();

        // Mengisi data hasil pencarian ke dalam tabel
        DefaultTableModel dataModel = (DefaultTableModel) table_data_karyawan.getModel();
        dataModel.setRowCount(0); // Mengosongkan tabel sebelum mengisi data baru

        while (resultSet.next()) {
            String bulan = resultSet.getString("bulan");
            String idKaryawan = resultSet.getString("id_karyawan");
            String nama = resultSet.getString("nama");
            String jenisKelamin = resultSet.getString("jenis_kelamin");
            String jenisKaryawan = resultSet.getString("jenis_karyawan");
            double gajiSetelahPotongan = resultSet.getDouble("gaji");

            // Format nilai gaji ke dalam format rupiah
            String gajiRupiah = String.format("Rp%,.2f", gajiSetelahPotongan);

            // Mengisi data ke dalam tabel
            Object[] rowData = {bulan, idKaryawan, nama, jenisKelamin, jenisKaryawan, gajiRupiah};
            dataModel.addRow(rowData);
        }

        if (dataModel.getRowCount() == 0) {
            JOptionPane.showMessageDialog(this, "Data tidak ditemukan.", "Informasi", JOptionPane.INFORMATION_MESSAGE);
        }
    } catch (SQLException ex) {
        Logger.getLogger(GUI_Restaurant.class.getName()).log(Level.SEVERE, null, ex);
        JOptionPane.showMessageDialog(this, "Error: " + ex.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
    }
    }//GEN-LAST:event_btnSearchActionPerformed

